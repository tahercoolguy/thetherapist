package com.master.design.therapist.DataModel;

public class Ethnic_details {

    private Boolean is_active;
    private String updated_at;
    private String ethnic_name;
    private String id;
    private String repeated_at;
    private String ethnic_name_as;

    public Boolean getIs_active() {
        return is_active;
    }

    public void setIs_active(Boolean is_active) {
        this.is_active = is_active;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }

    public String getEthnic_name() {
        return ethnic_name;
    }

    public void setEthnic_name(String ethnic_name) {
        this.ethnic_name = ethnic_name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRepeated_at() {
        return repeated_at;
    }

    public void setRepeated_at(String repeated_at) {
        this.repeated_at = repeated_at;
    }

    public String getEthnic_name_as() {
        return ethnic_name_as;
    }

    public void setEthnic_name_as(String ethnic_name_as) {
        this.ethnic_name_as = ethnic_name_as;
    }
}
