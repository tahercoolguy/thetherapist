package com.master.design.therapist.DataModel;

public class LoginUser {

    private String country;
    private String password;
    private String dob;
    private String name;
    private String interests_id;
    private String ethnicity_id;
    private String id;
    private String gender_id;
    private String about_you_id;
    private String email;
    private String confirm_password;


    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInterests_id() {
        return interests_id;
    }

    public void setInterests_id(String interests_id) {
        this.interests_id = interests_id;
    }

    public String getEthnicity_id() {
        return ethnicity_id;
    }

    public void setEthnicity_id(String ethnicity_id) {
        this.ethnicity_id = ethnicity_id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGender_id() {
        return gender_id;
    }

    public void setGender_id(String gender_id) {
        this.gender_id = gender_id;
    }

    public String getAbout_you_id() {
        return about_you_id;
    }

    public void setAbout_you_id(String about_you_id) {
        this.about_you_id = about_you_id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getConfirm_password() {
        return confirm_password;
    }

    public void setConfirm_password(String confirm_password) {
        this.confirm_password = confirm_password;
    }
}
