package com.master.design.therapist.DM;

public class SearchDM {
    private  String head;
    private  String des;

    public SearchDM(String head, String des) {
        this.head = head;
        this.des = des;
    }

    public String getHead() {
        return head;
    }

    public void setHead(String head) {
        this.head = head;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
}
